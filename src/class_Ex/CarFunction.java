package class_Ex;
// 2) 메서드의 컨테이너
// 2) 클래스의 두번째 역할 : 기능들을 담은 상자
public class CarFunction {
    // 메서드  : 기능(함수)을 가지는 클래스

    public void on() {
        System.out.println("시동키기");
    }

    public void off() {
        System.out.println("시동끄기");
    }

    public void go() {
        System.out.println("전진");
    }

    public void back() {
        System.out.println("후진");
    }
}
