package class_Ex;

public class Function {
    public void test() {
        System.out.println("test");
    }

    public String greet() {
        return "hello";
    }

    public int add(int a, int b) {
        int c = a + b;
        return c;
    }

    public void print(int x) {
        System.out.println(x);
    }

    public static void hi() {
        System.out.println("hi");
    }

}
